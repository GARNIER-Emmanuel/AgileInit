import Handlebars from 'handlebars';
import { AgileProject } from '../../project-init/domain/entities';

// Handlebars helpers
Handlebars.registerHelper('getPersonaName', function(personaId: string, options: any) {
  const project = options.data.root.project as AgileProject;
  return project.personas.find(p => p.id === personaId)?.name || 'Anonyme';
});

