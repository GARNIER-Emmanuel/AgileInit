export const generateDocs = (project: AgileProject) => {
  const context = {
    projectName: project.name,
    projectDescription: project.description,
    personas: project.personas,
    userStories: project.userStories,
    project: project, // For helper access
    currentDate: new Date().toLocaleDateString()
  };

  return {
    besoins: Handlebars.compile(templates.besoins)(context),
    user_stories: Handlebars.compile(templates.user_stories)(context),
    backlog: Handlebars.compile(templates.backlog)(context),
    stack: Handlebars.compile(templates.stack)(context),
    full: [
      Handlebars.compile(templates.besoins)(context),
      Handlebars.compile(templates.user_stories)(context),
      Handlebars.compile(templates.backlog)(context),
      Handlebars.compile(templates.stack)(context)
    ].join('\n\n---\n\n')
  };
};
